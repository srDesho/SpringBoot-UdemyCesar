package com.cristianml.modelos;

// Esta clase es la que va a tener la configuración de las categorías.

public class CategoriaRestModel {

	private Integer id;
	private String nombre;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
}
